class Movie {
  final int id;
  final String title;
  final String language;
  final String releaseDate;
  final String overview;
  final String poster;
  final String score;

  Movie({
    required this.id,
    required this.title,
    required this.language,
    required this.releaseDate,
    required this.overview,
    required this.poster,
    required this.score,
  });
}
